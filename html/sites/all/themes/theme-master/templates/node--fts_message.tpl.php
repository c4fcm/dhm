      Please distribute the following message to all of your cluster partners on behalf of the Financial Tracking Service in Geneva.<br />
      Thank You.</em>
      </p>

      <p>
      Dear <?php print $cluster_contact; ?>,<br /><br />

      As you may know, FTS is a global, on-line, real-time database of humanitarian funding needs and international contributions.  Its purpose is to improve resource allocation decisions and advocacy, by clearly indicating to what extent populations in crisis receive humanitarian aid, and in what proportion to needs. FTS depends on both donors and appealing organisations to provide the raw data that is reflected online (fts.unocha.org). This data is used for analytical and advocacy purposes throughout the year.<br /><br />  

