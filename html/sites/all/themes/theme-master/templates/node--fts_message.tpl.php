      For UN agencies, FTS has focal points in most UN agency headquarters, but it is still helpful if you check online to verify that the information is correct. All communication with FTS should be channelled through your HQ if possible.<br /><br />

      For NGOs, information can be shared in one of two ways:

      <ol>
